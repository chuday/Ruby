puts "Привет! Чтобы получить доступ к ячейке, необходимо ввести корректные данные"
print "Введите логин: "
login = gets.chomp

print "Введите пароль: "
password = gets.chomp

if login == 'admin' && password == '12345'
  puts "Поздравляю! Доступ к банковской ячейке разрешен!"
else
  puts "Вы ввели некорректные данные! Доступ закрыт, хранимое в ячейке уничтожено."
end

puts "-" * 40

# -------------------------------
# Задание 2 — Стоимость участка на Луне
# -------------------------------

print "Введите площадь участка, который хотите приобрести (в м²): "
plot_size = gets.to_i

if plot_size < 50
  puts "Участок стоит: $1000"
elsif plot_size <= 100
  puts "Участок стоит: $1500"
elsif plot_size > 100
  puts "Участок стоит: $#{plot_size * 25}"
else
  puts "Пожалуйста, введите корректные данные"
end

puts "-" * 40

# -------------------------------
# Задание 3 — Иммиграционный опрос
# -------------------------------

score = 0

puts "Привет! Это программа «Иммигрант»!"
print "Введите ваше имя: "
user_name = gets.chomp

puts "Ответьте на следующие вопросы (y/n):"

print "У вас есть высшее образование? "
education = gets.chomp.downcase

print "У вас есть опыт работы программистом? "
experience = gets.chomp.downcase

print "У вас более трёх лет опыта? "
experience_more_3_year = gets.chomp.downcase

# Подсчёт баллов
score += 1 if education == 'y'
score += 1 if experience == 'y'
score += 1 if experience_more_3_year == 'y'

puts

if score >= 2
  puts "Добро пожаловать, #{user_name}!"
else
  puts "#{user_name}, вы не набрали нужное количество баллов."
end

puts "Ваш итоговый балл: #{score} из 3"
